const ghToken = process.env.GITHUB_TOKEN;
const repoFull = process.env.GITHUB_REPOSITORY;

if (!ghToken) throw new Error("Missing GITHUB_TOKEN");
if (!repoFull || !repoFull.includes("/")) throw new Error("Missing or invalid GITHUB_REPOSITORY");

const [owner, repo] = repoFull.split("/");

async function gh(path, method = "GET", body) {
  const res = await fetch(`https://api.github.com${path}`, {
    method,
    headers: {
      Authorization: `Bearer ${ghToken}`,
      Accept: "application/vnd.github+json",
      "Content-Type": "application/json"
    },
    body: body ? JSON.stringify(body) : undefined
  });
  if (!res.ok) throw new Error(`${method} ${path} failed: ${res.status} ${await res.text()}`);
  if (res.status === 204) return null;
  return res.json();
}

const defaultBranch = process.env.GITHUB_REF_NAME || "main";
const sha = (await gh(`/repos/${owner}/${repo}/git/ref/heads/${defaultBranch}`)).object.sha;
const nonce = new Date().toISOString().replace(/[-:.TZ]/g, "");
const branch = `notion/proposal-${nonce}`;
const proposalPath = `proposals/notion-proposal-${nonce}.md`;

await gh(`/repos/${owner}/${repo}/git/refs`, "POST", {
  ref: `refs/heads/${branch}`,
  sha
});

const content = Buffer.from(
  `# Proposal\n\nGenerated through the Notion adapter.\n\nLocal authority: HyperAI.\n`
).toString("base64");

await gh(`/repos/${owner}/${repo}/contents/${proposalPath}`, "PUT", {
  message: "add proposal from Notion adapter",
  content,
  branch
});

const pr = await gh(`/repos/${owner}/${repo}/pulls`, "POST", {
  title: "Proposal from Notion adapter",
  head: branch,
  base: defaultBranch,
  body: "Generated by an optional Notion adapter. Local HyperAI remains the root authority."
});

console.log(`PR_CREATED ${pr.html_url}`);
